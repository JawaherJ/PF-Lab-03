#include <stdio.h>

int main() {
    
    int age = 18;
    float height = 5.8f;
    double gpa = 3.75432;
    char grade = 'A';

   
    printf("Integer (age): %d\n", age);        
    printf("Float (height): %.2f\n", height);  
    printf("Double (GPA): %.5lf\n", gpa);      
    printf("Character (grade): %c\n", grade);  

    return 0;
}
